// Introduction in DOM in javaScript
// DOM [Document Object Model]
// in javascript represent the structure of an HTML document as a tree of objects. it allows to javaScript to intract with and manipulate the elements, structure, and content of a web pages.

// Access and changes the structure of the HTML elements.
// add, remove, or modify elements.
// Handle Events (click or form submition)

// DOM Structure:
// <html> is the root node.
// <head>, <body>. and all the other tags are child node of <html>

// Nodes:
// Elements Node: <div>, <p>, <h1>
// Text Node : represnt the text inside the HTML tags.
// Attribute Node: class and id

// Accessing Elements are:
// getElementById(): Access an element by it ID attribute.
// example:

// getElementByClassName(): Access an element by their class attribute.
// getElementByTagName(): Access an element by their tag name.
// querySelector() : Access the first element that matches a CSS selector.
// querySelectorAll() : Access all elements that match a CSS selector.

// Modifying Elements
// You can modify the content, attributes, or style of an element.

// textContent: Change the text inside an element.
// innerHTML: Change the HTML inside an element.
// setAttribute(): Modify an attribute of an element.
// style: Modify the CSS style of an element.


// Creating and Removing Elements
// You can dynamically create and delete elements in the DOM.
// createElement(): Create a new element.
// appendChild(): Add a new element to the DOM.
// removeChild(): Remove an element from the DOM.

// Event Handling
// You can attach events (such as clicks, keypresses, etc.) to DOM elements.

// addEventListener(): Attach an event listener to an element.
// removeEventListener(): Remove an event listener.